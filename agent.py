    async def _handle_response(self, response) -> dict:
        result = {"text": "", "files": [], "success": True, "buttons": None}

        for block in response.content:
            if block.type == "text":
                result["text"] += block.text

            elif block.type == "tool_use":
                tool_result = await self._execute_tool(block.name, block.input)

                if tool_result.get("file"):
                    result["files"].append({
                        "file":       tool_result["file"],
                        "filename":   tool_result["filename"],
                        "drive_link": tool_result.get("drive_link", ""),
                    })

                for f_path, f_name, f_link in zip(
                    tool_result.get("extra_files", []),
                    tool_result.get("extra_names", []),
                    tool_result.get("extra_links", [""] * len(tool_result.get("extra_files", []))),
                ):
                    if Path(f_path).exists():
                        result["files"].append({
                            "file":       f_path,
                            "filename":   f_name,
                            "drive_link": f_link,
                        })

                if tool_result.get("error"):
                    # Ошибка от инструмента (например, попытка создать сделку
                    # с уже существующим номером). Показываем пользователю
                    # напрямую — без "✅" префикса, текст уже содержит нужные значки.
                    result["text"] += f"\n{tool_result['error']}"
                elif tool_result.get("buttons"):
                    result["buttons"] = tool_result["buttons"]
                    if tool_result.get("message"):
                        result["text"] += f"\n{tool_result['message']}"
                elif tool_result.get("message"):
                    msg = tool_result["message"]
                    prefix = "" if msg.startswith(("⚠️", "❌", "✅")) else "✅ "
                    result["text"] += f"\n{prefix}{msg}"

        return result

    async def _execute_tool(self, tool_name: str, tool_input: dict) -> dict:

        if tool_name == "create_contract":
            contract_date = tool_input.get("contract_date") or None
            existing_number = tool_input.get("existing_contract_number", "").strip()
            is_regen = bool(existing_number)

            if is_regen:
                number = existing_number
                logger.info(f"Перегенерация документов для существующей сделки {number}")
            else:
                number = await self.drive.get_next_contract_number(contract_date)

                # ── Защита от перезаписи существующей сделки ────────────
                existing = await self.sheets.get_deal(number)
